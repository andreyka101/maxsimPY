a=int(input())
a1=0
c=[]
while a1!=a:
    c.append(int(input()))
    a1+=1
bb=0
while c!=[]:
    a=c[0]
    c.remove(a)
    vv=0
    for i in c:
        if a!=0 and i>=a and i>0:
            c[vv]=i-1
        vv+=1
    a=a-1
    if a>=0:
        c.append(a)
    bb+=1
print(bb)
